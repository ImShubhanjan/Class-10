public class forDemo {
    public static void main(String[] args) 
    {
        for (int i = 5; i >= 1; i--) //Descending Order 
        {
            System.out.println(i);
        }

        for (int i = 1; i <= 5; i++) //Ascending Order
        {
            System.out.println(i);    
        }
    }
}
